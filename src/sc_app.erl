%% Func: start/2
%% Returns: {ok, Pid}        |
%%          {ok, Pid, State} |
%%          {error, Reason}   
%%--------------------------------------------------------------------
start(_Type, StartArgs) ->
	sc_store:init(),
	case sc_sup:start_link(StartArgs) of
		{ok, Pid} -> 
			{ok, Pid};
		Error ->
			Error
	end.

%%--------------------------------------------------------------------
%% Func: stop/1
%% Returns: any 
%%--------------------------------------------------------------------
